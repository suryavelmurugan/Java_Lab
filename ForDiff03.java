import java.io.*;
class ForDiff03
{

    public static void main(String[] args)
    {
        //   initializing count value
        int  count=10;
        int n=1;
        // perfroming for loop
        for (int i=1;i<=count;i++)
        { 
            n=n+3;
            // printing value n with space using \t 
            System.out.print(n+"\t") ;
            
        }
    }
}

